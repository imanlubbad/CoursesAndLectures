package com.m_learning.network.model;

import java.io.Serializable;

public class Lecture implements Serializable {
    String id;
    String title;
    String description;
    String image;
    String lectureVideo;
    int isActive;
    boolean isDeleted;
    long createdAt;
    String lecturerId;
    String lecturerName;
    String courseId;
    int viewsNo;

    public String getCourseId() {
        return courseId;
    }

    public void setCourseId(String courseId) {
        this.courseId = courseId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLectureVideo() {
        return lectureVideo;
    }

    public void setLectureVideo(String lectureVideo) {
        this.lectureVideo = lectureVideo;
    }

    public int getIsActive() {
        return isActive;
    }

    public void setIsActive(int isActive) {
        this.isActive = isActive;
    }

    public boolean getIsDeleted() {
        return isDeleted;
    }

    public void setIsDeleted(boolean isDeleted) {
        this.isDeleted = isDeleted;
    }

    public long getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(long createdAt) {
        this.createdAt = createdAt;
    }

    public String getLecturerId() {
        return lecturerId;
    }

    public void setLecturerId(String lecturerId) {
        this.lecturerId = lecturerId;
    }

    public String getLecturerName() {
        return lecturerName;
    }

    public void setLecturerName(String lecturerName) {
        this.lecturerName = lecturerName;
    }

    public int getViewsNo() {
        return viewsNo;
    }

    public void setViewsNo(int viewsNo) {
        this.viewsNo = viewsNo;
    }


}
